numberToColor = num => {
  switch (num) {
    case 0:
      // return `gray`;
      return `#202020`;
    case 1:
      return `white`;
    case 2:
      return `yellow`;
    case 3:
      // return `green`;
      return `lime`;
    case 4:
      return `blue`;
    case 5:
      return `red`;
    case 6:
      return `orange`;
  }
}